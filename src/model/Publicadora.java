package model;

public class Publicadora {
    private String id;
    private String nome;
    private String local;

    // Getters
    public String getId() {
        return id;
    }
    
    public String getNome() {
        return nome;
    }
    
    public String getLocal() {
        return local;
    }

    // Setters
    public void setId(String id) {
        this.id = id;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setLocal(String local) {
        this.local = local;
    }
}
